          constraints: BoxConstraints(
            minHeight: cons.maxHeight,
          ),
          child: SingleChildScrollView(
            child: Column(
              children: [
                // sing up screen header
                SizedBox(
                  width: MediaQuery.of(context).size.width,
                  height: 285,
